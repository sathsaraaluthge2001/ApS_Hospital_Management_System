    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String PatientIdtxt;
        int PatientId;

        PatientIdtxt=txtSearch.getText();

        if(PatientIdtxt.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "fill the search field", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            PatientId=Integer.parseInt(txtSearch.getText());

            try {
                pst=con.prepareStatement("select PatientName , Gender , DOF,ContactNo,Address,Age,Email from patient where PatientId=?");
                pst.setInt(1, PatientId);
                ResultSet rs=pst.executeQuery();

                if(rs.next()==true){
                    String PatientName=rs.getString(1);
                    String Gender=rs.getString(2);
                    String DOF=rs.getString(3);
                    String ContactNo=rs.getString(4);
                    String Address=rs.getString(5);
                    String Age=rs.getString(6);
                    String Email=rs.getString(7);

                    txtPaName.setText(PatientName);
                    txtGender.setText(Gender);
                    txtDOF.setText(DOF);
                    txtCoNo.setText(ContactNo);
                    txtAge.setText(Age);
                    txtEmail.setText(Email);
                    txtAdress.setText(Address);

                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "enter valid Id", "Success", JOptionPane.INFORMATION_MESSAGE);
                    txtAdress.setText("");
                    txtAge.setText("");
                    txtCoNo.setText("");
                    txtDOF.setText("");
                    txtEmail.setText("");
                    txtGender.setText("");
                    txtPaName.setText("");
                }
            } catch (SQLException ex) {
                System.out.println("sQL STATEMENT ERROR "+ex.getMessage());
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

